let grade ="F";
switch(grade){
    case "A":
        console.log("You did Great!");
        break;
    case "B":
        console.log("You did Good!");
        break;
    case "C":
        console.log("You did Okay!");
        break;
    case "D":
        console.log("You Passed.....Barely");
        break;
    case "F":
        console.log("You Failed!");
        break;
    default:
        console.log(grade," is not a letter");
}